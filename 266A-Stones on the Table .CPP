#include <iostream>
#include<string>
using namespace std;

int main() {
 int n,r=0,g=0,b=0;
 cin>>n;
 string a;
 cin>>a;
 for(int i=1;i<n;i++){
 	if(a[i-1]==a[i])
 	if(a[i]=='R')++r;
 	else if(a[i]=='G'){
 		++g;
 	}
 	else if(a[i]=='B')
 	++b;
 }
 cout<<r+g+b;
  	return 0;
}
