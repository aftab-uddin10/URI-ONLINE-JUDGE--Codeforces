#include<iostream>
using namespace std;
int main()
{
    int n,m,i,j,h;
    while(cin>>n>>h){
        int a[n+1],s=0,b=0;
        for(i=0;i<n;i++){
            cin>>a[i];
            if(a[i]>h)
               b++;
            else
                s++;
 
        }
        cout<<2*b+s<<endl;
    }
    return 0;
}
