#include <iostream>
using namespace std;
int main() {
   int k,n,w,i,s=0;
   cin >>k>>n>>w ;
   for(i=1;i<=w;i++){
       s=s+(k*i);
   }
   if(s<=n) cout <<"0"<<endl;
   else cout <<s-n<<endl;

    return 0;
}
